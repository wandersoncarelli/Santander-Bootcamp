// Tratamento de números
let num = 100;

console.log(num + 3);
console.log(num / 5);
console.log(num % 2);

// Biblioteca Math
console.log(typeof Math);
console.log(Math.PI);

let fiveByThree = 5 / 3;

console.log(fiveByThree);
console.log(Math.floor(fiveByThree)); // Método "floor" arredonda o número para baixo
console.log(Math.ceil(fiveByThree)); // Método "ceil" arredonda o número para cima

let percent = 10;

console.log(percent + "%");
console.log(typeof percent);

percent = percent.toString(); // Converte a variável "percent" pro tipo "String"
console.log(typeof percent);
